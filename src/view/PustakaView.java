    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
       int getAnswer = JOptionPane.showConfirmDialog(rootPane, "Apakah Anda Yakin ingin Menghapus Data ??", "Konfirmasi", JOptionPane.YES_NO_OPTION);
       
        if (getAnswer == JOptionPane.YES_OPTION) {
            try{
                pustakaControl.deleteDataPustaka(nidInput.getText());
                clearText();
                showPustaka();
                JOptionPane.showMessageDialog(null, "Data Berhasil dihapus");
            }catch(Exception e){
                System.out.println("Error : " + e.getMessage());
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        setEditDeleteBtn(true);
        
        setComponent(false);
        
        Pustaka pustaka = pustakaControl.searchDataPustaka(searchInput.getText());
        
        try{
            if (pustaka == null) {
                clearText();
                setEditDeleteBtn(false);
                JOptionPane.showConfirmDialog(null, "Data Pustaka Tidak ditemukan !!!", "Konfirmasi", JOptionPane.DEFAULT_OPTION);


            }else{
                nidInput.setText(pustaka.getIdPustaka());
                judulInput.setText(pustaka.getJudul());
                TTInput.setText(pustaka.getTahunTerbit());
                penerbitInput.setText(pustaka.getPenerbit());
                
                if ("Buku".equals(pustaka.getJenis())) {
                    
                    String ed = "" +pustaka.getEdisi()+ "";
                    
                    edisiInput.setText(ed);
                }else{
                    String vol = "" +pustaka.getVolume()+ "";
                    
                    volumeInput.setText(vol);
                }

            }
        
        }catch (Exception e){
            System.out.println("Error : " +e.getMessage());
        }
        
    }//GEN-LAST:event_searchBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        
        try{
            inputKosongException();
            //noIdPustakaException();
            Pustaka p;
            
            if ("Buku".equals(jenis)) {
                p = new Pustaka(nidInput.getText(), judulInput.getText(), jenis,  TTInput.getText(), penerbitInput.getText(),
                    Integer.parseInt(edisiInput.getText()), 
                    0);
            }else{
                p = new Pustaka(nidInput.getText(), judulInput.getText(), jenis,  TTInput.getText(), penerbitInput.getText(),
                    0, 
                    Integer.parseInt(volumeInput.getText()));
            }
            
            
            
            
            if ("Tambah".equals(action)) {
                pustakaControl.insertDataPustaka(p);
                JOptionPane.showMessageDialog(this, "[*] Berhasil menambahkan " + jenis + "[*] ");
                
            }else if ("Ubah".equals(action)) {
                pustakaControl.updateDataPustaka(p, nidInput.getText());
                JOptionPane.showMessageDialog(this, "[*] Berhasil mengubah " + jenis + "[*] ");
            }
            
            clearText();
            showPustaka();
            setComponent(false);
           
            
        }catch(InputKosongException e){
            JOptionPane.showMessageDialog(this, e.message());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        
        
    }//GEN-LAST:event_saveBtnActionPerformed

